# 함수 기능 : 원하는 단의 구구단을 출력해주는 기능 함수
# 함수 이름 : gugudan
# 매개 변수 : dan
# 함수 결과 : 
#---------------------------------------------------------------------------------------------------------------
def gugudan ():
    dan= int(input("원하는 단을 입력하세요 : "))
    print(f'----{dan}단----')
    for n in range(1,10):
        print(f'{dan} * {n} = {dan*n:>2}')

gugudan()

