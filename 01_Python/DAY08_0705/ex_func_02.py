# 함수 기능 : 파일의 확장자를 반환해주는 기능 함수
# 함수 이름 : f_extension
# 매개 변수 : file_name
# 함수 결과 : 확장자 str
#---------------------------------------------------------------------------------------------------------------
def f_extension(file_name):
    dot_idx=file_name.rfind('.')
    result=file_name[dot_idx+1:]
    return result


print(f_extension("kakao.jpg"))