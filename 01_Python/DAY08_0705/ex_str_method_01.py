# 문자열의 뒷부분부터 찾는 메서드 ==> rfrind(), rindex()
#------------------------------------------------------------------------------------------
msg="Happy"
#    01234
# -첫번째 'p'인덱스 찾기
idx=msg.rfind('p')
print(f'p의 인덱스 : {idx}')

# -두번째 'p'인덱스 찾기
#msg="Happy"
#    012
idx=msg.rfind('p',0,idx) # find(찾을문자,시작인덱스,끝인덱스+1)
print(f'p의 인덱스 : {idx}')

