# 모듈로딩 ----------------------------------------------------------------------------------------------------------------------------------------

from flask import Flask, render_template

# 사용자 정의 함수 ---------------------------------------------------------------------------------------------------------------------------------
def create_app():
    # 전역변수 -------------------------------------------------------------------------------------------------------------------------------------
    # Flask Web Server 인스턴스 생성
    APP=Flask(__name__)

    # 라우팅 기능 함수 -----------------------------------------------------------------------------------------------------------------------------
    # @Flask Web Server 인스턴수변수명.route("URL)
    # http://127.0.0.1:5000/
    @APP.route("/")
    def main():
        return render_template("main.html")

        
    @APP.route("/ml")
    @APP.route("/ml/")
    def ml():
        return render_template("ml.html")

    @APP.route("/dl")
    @APP.route("/dl/")
    def dl():
        return render_template("dl.html")
    
    @APP.route("/vi")
    @APP.route("/vi/")
    def vi():
        return render_template("vi.html")

    def goHome():
        return APP.redirect("/main")

    return APP




# 조건부 실행 -----------------------------------------------------------------------------------------------------------------------------------  
if __name__=='__main__':
    #Flask Web Server 구동
    app=create_app()
    app.run()

